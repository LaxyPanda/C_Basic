#include<stdio.h>
int main(){
    // Arithnetic operator
    // + (add)
    // - (Sub)
    // * (multi)
    // / (divi)
    // % (modulus)
    // ++ increment
    // -- decrement

    int a = 10 ;
    int b = 20 ;

    int c = a + b;
    printf("C= %d",c);

    int e = a++;
    int d = b--;
    printf("\nD= %d \nE= %d",d,e);

}